class CatAndRat {
public:
	double getTime(int R, int T, int Vrat, int Vcat) {
		double dis;
		if(Vrat >= Vcat) return -1.0;
		if((double)(Vrat * T) > (double)(PI * R))
			dis = PI*R;
		else
			dis = Vrat*T;
		return (dis / double(Vcat - Vrat));
	}
};


