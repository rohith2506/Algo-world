class PastingPaintingDivTwo {
    public:
    long long countColors(vector<string> clipboard, int T) {
       long long int total = 0, last_diff = 0;
       vector<vector<char> > board(170, vector<char>(170, '.'));
       for(int k=0; k<100; k++) {
         long long int cur_total = total;
         for(int i=0;i<clipboard.size(); i++) {
            for(int j=0; j<clipboard[i].size(); j++) {
                if(clipboard[i][j] == 'B') {
                        if(board[i+k][j+k] == '.')
                                total++;
                        board[i+k][j+k] = 'B';
                }
            }
         }
         if(k + 1 == T) return total;
         last_diff = total - cur_total;
       }
       return (total + ((T - 100) * last_diff));
    }
};

