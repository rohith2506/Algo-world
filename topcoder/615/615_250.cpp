class AmebaDiv2 {
public:
	int simulate(vector <int>, int);
};

int AmebaDiv2::simulate(vector <int> X, int A) {

	int size = A;
	for(int i=0;i<X.size();i++){
		if(X[i] == size)
			size = size + X[i];
	}
	return size;	
}


