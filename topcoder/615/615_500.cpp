class LongLongTripDiv2 {
public:
	string isAble(long long, int, int);
};

string LongLongTripDiv2::isAble(long long D, int T, int B) {
	if(((D - T) < 0) || ( ( D - T) % ( B - 1) != 0) || ( ( D - T)/(B - 1) > T) )
		return "Impossible";
	else
		return "Possible";
}

